#include "asm.h"

u_int32_t	u_int_32_to_big_endian(u_int32_t n)
{
	return ((n & 0xFF000000) >> 24 | (n & 0x00FF0000) >> 8 | \
	(n & 0x0000FF00) << 8 | (n & 0x000000FF) << 24);
}
